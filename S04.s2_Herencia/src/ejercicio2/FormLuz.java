    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularActionPerformed
        Luz z = new Luz(3.30);
        z.setConsumo(Double.parseDouble(txtConsumo.getText()));
        z.setTipo(cbxTipo.getSelectedItem().toString());
        z.caclularTarifa();
        z.calcularRecarga();
        z.calcularMontoPagar();
        txtSalida.append("Cargo fijo "+z.getCargoFijo()+"\n");
        txtSalida.append("Consumo "+z.getConsumo()+"\n");
        txtSalida.append("Tipo "+z.getTipo()+"\n");
        txtSalida.append("Tarifa "+z.getTarifa()+"\n");
        txtSalida.append("Recarga "+z.getRecarga()+"\n");
        txtSalida.append("Monto a pagar "+z.getMontoPagar()+"\n");
    }//GEN-LAST:event_btnCalcularActionPerformed

